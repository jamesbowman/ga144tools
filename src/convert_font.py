def convert_font(font):
    ret = []
    for i in range(0, len(font), 5):
        char = [0]*7
        for y in range(7):
            for x in range(5):
                if (data[i + x] << y) & 0x80:
                    char[y] = char[y] | 1<< (5 - x)
        ret.extend(char)
    return ret

